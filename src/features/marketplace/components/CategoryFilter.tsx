/**
 * Category Filter Component
 * Sidebar filter for marketplace categories
 */
import { cn } from '@/lib/utils';
import { CATEGORY_INFO, type AppCategory } from '../types';

interface CategoryFilterProps {
  categories: Array<{ category: AppCategory; count: number }>;
  selected?: AppCategory;
  onSelect: (category: AppCategory | undefined) => void;
}

export function CategoryFilter({ categories, selected, onSelect }: CategoryFilterProps) {
  // Sort categories by count
  const sortedCategories = [...categories].sort((a, b) => b.count - a.count);

  return (
    <div className="space-y-2">
      <h3 className="text-sm font-medium text-text-primary mb-3">Categories</h3>

      {/* All Apps */}
      <button
        onClick={() => onSelect(undefined)}
        className={cn(
          'w-full flex items-center justify-between px-3 py-2 text-sm rounded-md transition-colors',
          !selected
            ? 'bg-primary/10 text-primary font-medium'
            : 'text-text-secondary hover:bg-bg-muted'
        )}
      >
        <span>All Apps</span>
        <span className="text-xs text-text-muted">
          {sortedCategories.reduce((sum, c) => sum + c.count, 0)}
        </span>
      </button>

      {/* Category List */}
      <div className="space-y-1">
        {sortedCategories.map(({ category, count }) => {
          const info = CATEGORY_INFO[category];
          const isSelected = selected === category;

          return (
            <button
              key={category}
              onClick={() => onSelect(isSelected ? undefined : category)}
              className={cn(
                'w-full flex items-center justify-between px-3 py-2 text-sm rounded-md transition-colors',
                isSelected
                  ? 'bg-primary/10 text-primary font-medium'
                  : 'text-text-secondary hover:bg-bg-muted'
              )}
            >
              <span className="flex items-center gap-2">
                <CategoryIcon icon={info.icon} className="w-4 h-4" />
                <span>{info.label}</span>
              </span>
              <span className="text-xs text-text-muted">{count}</span>
            </button>
          );
        })}
      </div>
    </div>
  );
}

